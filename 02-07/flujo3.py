"""Conversion de datos"""
"""conversion de int a str"""
var1 = "2023"
var2 = str(var1)

print("El valor de mi variable var2 es: {}".format(var2))
print("El tipo de dato de mi variable var2 es: {}".format(type(var2)))

"""conversion de int a flotante"""

var3 = 1990
var4 = float(var3)

print("El valor de mi variable var4 es: {}".format(var4))
print("El tipo de dato de mi variable var4 es: {}".format(type(var4)))