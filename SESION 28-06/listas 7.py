"""Lista: Cantidad de veces que se repite el elemento en un lista"""

varlista = ["amaru", "topo", "mancha"]

varlista.append("amaru")
varlista.append("topo")
varlista.append("mancha")

print("Mi nueva lista es: {}".format(varlista))

print("La cantidad de veces que se repite 'amaru': {}".format(varlista.count("amaru")))
print("La cantidad de veces que se repite 'topo': {}".format(varlista.count("topo")))