
"""Operadores aritmeticos en Python"""
"""Potencia"""

base = 4
potencia = 2

resultado = base**potencia

print("El resultado de la potencia es: {}".format(resultado))


"""Usando la palabra reservada "pow" podemos tambien elevar a cierta potencia un numero"""
"""Nos pedira 2 valores, el primero es la base y el segundo es la potencia a ser elevada la base"""

potencia1 = pow(30,2)
potencia2 = pow(2,12)


print("EL valor de la potencia es: {}".format(potencia1))
